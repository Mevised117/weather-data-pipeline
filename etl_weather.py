def transform_data(raw_data):
    if not raw_data:
        return None
    
    print("üßπ ‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏à‡∏±‡∏î‡∏£‡∏∞‡πÄ‡∏ö‡∏µ‡∏¢‡∏ö‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•...")
    # ‡∏î‡∏∂‡∏á‡πÄ‡∏â‡∏û‡∏≤‡∏∞‡∏ó‡∏µ‡πà‡πÄ‡∏£‡∏≤‡∏ï‡πâ‡∏≠‡∏á‡∏Å‡∏≤‡∏£ ‡πÉ‡∏´‡πâ‡∏ï‡∏£‡∏á‡∏Å‡∏±‡∏ö Schema ‡πÉ‡∏ô BigQuery
    record = {
        "city": raw_data["name"],
        "temp": raw_data["main"]["temp"],
        "humidity": raw_data["main"]["humidity"],
        # ‡πÅ‡∏õ‡∏•‡∏á‡πÄ‡∏ß‡∏•‡∏≤‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô‡πÉ‡∏´‡πâ‡πÄ‡∏õ‡πá‡∏ô Format ‡∏ó‡∏µ‡πà BigQuery ‡πÄ‡∏Ç‡πâ‡∏≤‡πÉ‡∏à
        "timestamp": datetime.now().isoformat()
    }
    return record

# ==========================================
# 3. LOAD: ‡∏™‡πà‡∏á‡∏Ç‡∏∂‡πâ‡∏ô BigQuery (‡πÅ‡∏ö‡∏ö Batch Load - ‡∏ü‡∏£‡∏µ)
# ==========================================
def load_to_bigquery(data):
    if not data:
        return

    print("üöÄ ‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏™‡πà‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏Ç‡∏∂‡πâ‡∏ô Google BigQuery (‡πÅ‡∏ö‡∏ö Load Job)...")
    
    # 1. ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏Å‡∏≤‡∏£‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠
    credentials = service_account.Credentials.from_service_account_file(KEY_FILE_PATH)
